      $.post("<?= base_url('Master/get_dept_counter') ?>", {
          dept_id
        },
        function(data) {
          if (data != "") {
           var result = '';
            $.each(data, function(i, item) {

              if(unt_val == item.m_cashacc_id){
                var op = 'selected';
              }else {
                var op = '';
              }

              result +=
                '<option value="' +
                item.m_cashacc_id +
                '" '+op+' >' +
                item.m_cashacc_name +
                "</option>";
            });

            $("#m_cashcount").append(result);
          } else {
            $("#m_cashcount").html("");
          }
        },
        "json"
      );
    });


